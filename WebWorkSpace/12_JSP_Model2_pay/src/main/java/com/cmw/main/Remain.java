package com.cmw.main;

public class Remain {
	private int earn;
	private int spend;
	private int result;
	
	public Remain() {
	}

	public Remain(int earn, int spend, int result) {
		super();
		this.earn = earn;
		this.spend = spend;
		this.result = result;
	}

	public int getEarn() {
		return earn;
	}

	public void setEarn(int earn) {
		this.earn = earn;
	}

	public int getSpend() {
		return spend;
	}

	public void setSpend(int spend) {
		this.spend = spend;
	}

	public int getResult() {
		return result;
	}

	public void setResult(int result) {
		this.result = result;
	}
	
	
	
}
